#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int i = 0;
	if (s == NULL)
		return (write(1, "(null)", 6));
	while (*s)
	{
		i += ft_putchar(*s);
		s++;
	}
	return (i);
}